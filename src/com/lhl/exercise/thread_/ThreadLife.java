public class ThreadLife {
    /*
     *  线程的生命周期
     *  1. NEW
     *      新建状态
     *  2. RUNNABLE
     *      可运行状态
     *      细分的话，又可分为两个子状态
     *          2.1 就绪状态
     *          2.2 运行状态
     *  3. BLOCKED
     *      阻塞状态
     *      遇到锁之后进入阻塞状态
     *  4. WAITING
     *      等待状态
     *      无限期等待，没有时长限定
     *  5. TIMED_WAITING
     *      超时等待状态
     *      这种等待是有时长限定的
     *  6. TERMINATED
     *      终止状态（死亡状态）
     * */
}
